const mongoose = require("mongoose");

const appoinmentSchema = new mongoose.Schema(
  {
    userId: {
      type: String,
      required: true,
    },
    doctorId: {
      type: String,
      required: true,
    },
    doctorInfo: {
      type: String,
      required: true,
    },
    userInfo: {
      type: String,
      required: true,
    },
    date: {
      type: String,
      required: true,
    },
    time: {
      type: String,
      required: true,
    },
    status: {
      type: String,
      required: true,
      default: "pening",
    },
  },
  {
    timestamps: true,
  }
);

const appoinmentModel = mongoose.model("appoinment", appoinmentSchema);

module.exports = appoinmentModel;
